package json.rasp;

public class Search {
    public String date;
    public To to;
    public From from;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public To getTo() {
        return to;
    }

    public void setTo(To to) {
        this.to = to;
    }

    public From getFrom() {
        return from;
    }

    public void setFrom(From from) {
        this.from = from;
    }
}
